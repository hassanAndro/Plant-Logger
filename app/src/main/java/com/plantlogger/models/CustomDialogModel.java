public class CustomDialogModel {
    String a_min;
    String a_max;
    String last_value;
    String name;
    String date_time;

    public String getA_min() {
        return a_min;
    }

    public void setA_min(String a_min) {
        this.a_min = a_min;
    }

    public String getA_max() {
        return a_max;
    }

    public void setA_max(String a_max) {
        this.a_max = a_max;
    }

    public String getLast_value() {
        return last_value;
    }

    public void setLast_value(String last_value) {
        this.last_value = last_value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate_time() {
        return date_time;
    }

    public void setDate_time(String date_time) {
        this.date_time = date_time;
    }
}
